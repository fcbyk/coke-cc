#include <iostream>
using namespace std;
int main(){
    int a;
    cin >> a;
    switch (a) {
        case 1: cout << "Monday" << endl;
        case 2: cout << "Tuesday" << endl;
        case 3: cout << "Wednesday" << endl;
        case 4: cout << "Thursday" << endl;
        case 5: cout << "Friday" << endl;
        case 6: cout << "Saturday" << endl;
        case 7: cout << "Sunday" << endl;
    }
    return 0;
}