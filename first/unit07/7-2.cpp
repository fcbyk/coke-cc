// 写法一：多个独立if + return
#include <iostream>
using namespace std;

int main() {
    // 1. 准备变量：用来存放三条边的长度
    int a, b, c;

    // 2. 获取用户输入
    cout << "输入三角形的三条边: ";
    cin >> a >> b >> c;  // 例如输入：3 4 5

    // 3. 首先检查是否能构成三角形（最重要！）
    // 规则：任意两边相加必须大于第三边
    if (a + b <= c || a + c <= b || b + c <= a) {
        cout << "无法构成三角形！" << endl;
        return 0; // 结束程序
    }

    // 4. 判断等边三角形（最简单的情况）
    // 特征：三条边都相等 → 3 3 3
    if (a == b && b == c) {
        cout << "这是等边三角形！" << endl;
        return 0;
    }

    // 5. 判断等腰三角形
    // 特征：只有两条边相等 → 3 3 4 或 3 4 4
    if (a == b || b == c || a == c) { // 任意两边相等即可
        cout << "这是等腰三角形！" << endl;
        return 0;
    }

    // 6. 如果都不是，就是普通三角形
    // 特征：三条边都不相等 → 3 4 5
    cout << "这是普通三角形。" << endl;

    return 0; // 程序结束
}